String day = "Monday";
switch (day) {
  case "Monday":
    print("It's the start of the week!");
    break;
  case "Tuesday":
    print("It's the second day of the week.");
    break;
  case "Wednesday":
    print("It's the middle of the week.");
    break;
  case "Thursday":
    print("It's almost the weekend.");
    break;
  case "Friday":
    print("It's the last day of the workweek!");
    break;
  case "Saturday":
    print("It's the weekend!");
    break;
  case "Sunday":
    print("It's the last day of the weekend.");
    break;
  default:
    print("Invalid day.");